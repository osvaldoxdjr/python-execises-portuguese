"""
Faça um Programa que pergunte quanto você ganha por hora e o número de horas
trabalhadas no mês.
Calcule e mostre o total do seu salário no referido mês.
"""

sal_hora = int(input("Qual seu salario/hora? "))
trab_mes = int(input("Qual o numero de horas trabalhadas no mes? "))
print("Voce ganhou nesse mes R$", sal_hora*trab_mes)
